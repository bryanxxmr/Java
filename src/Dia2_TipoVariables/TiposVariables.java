package Dia2_TipoVariables;

public class TiposVariables {
    public static void main (String[] args)
    {
        //Declaracion de variables
        String nombre = "Kenny Bryan";
        int edad=35;
        String profesion ="QA Automation";
        boolean aprendiendoJava = true;

        System.out.println(nombre);
        //combinando texto fijo con variables
        System.out.println("Hola, soy " + nombre);
        System.out.println("Tengo " + edad);
        System.out.println("Soy " + profesion);
        System.out.println("Estoy aprendiendo JAVA: " + aprendiendoJava);
    }
}
