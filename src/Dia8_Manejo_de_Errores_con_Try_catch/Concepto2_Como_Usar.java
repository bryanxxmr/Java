package Dia8_Manejo_de_Errores_con_Try_catch;

public class Concepto2_Como_Usar {
    public static void main(String[] args) {
        //El try/catch es como una red de seguridad. Le dices a Java:
        //
        //try → "intenta ejecutar este código"
        //catch → "si algo falla, haz esto en vez de caerte"

        try {
            //codigo qe podria fallar
        }
        catch (Exception e) {
            //lo que haces si falla
        }
    }
}
